// ═══════════════════════════════════════════════════════════
function setupGuildDefaults(guildId) {

  // ── الردود التلقائية ────────────────────────────────────
  const existingReplies = db.prepare('SELECT COUNT(*) as c FROM auto_replies WHERE guild_id=?').get(guildId);
  if (existingReplies.c === 0) {
    const ins = db.prepare(
      "INSERT INTO auto_replies (guild_id, keyword, responses, cooldown, created_by) VALUES (?,?,?,?,'البوت')"
    );
    for (const r of DEFAULT_REPLIES) {
      try { ins.run(guildId, r.keyword, r.responses, r.cooldown); } catch (_) {}
    }
    console.log(`[Setup] ✅ ${DEFAULT_REPLIES.length} رد تلقائي → ${guildId}`);
  }

  // ── قوالب التذكيرات ────────────────────────────────────
  const existingReminders = db.prepare("SELECT COUNT(*) as c FROM reminders WHERE guild_id=? AND is_template=1").get(guildId);
  if (existingReminders.c === 0) {
    const ins = db.prepare(
      "INSERT INTO reminders (guild_id, channel_id, title, message, interval_minutes, enabled, is_template, created_by) VALUES (?,'0',?,?,?,0,1,'البوت')"
    );
    for (const t of DEFAULT_REMINDER_TEMPLATES) {
      try { ins.run(guildId, t.title, t.message, t.interval_minutes); } catch (_) {}
    }
    console.log(`[Setup] ✅ ${DEFAULT_REMINDER_TEMPLATES.length} قالب تذكير ديني → ${guildId}`);
  }
}

module.exports = { setupGuildDefaults };
